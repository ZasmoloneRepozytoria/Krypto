class AesEncrypter(val key: AesKey) : Encrypter, Decrypter {
    // Tutaj tylko szyfrowanie i deszyfrowanie danych w postaci bajt√≥w.

    /**
     * Dodaje klucz rundy [round] do [state].
     */
    private fun addRoundKey(state: Array<UByteArray>, roundKeys: Array<UByteArray>, round: Int): Array<UByteArray> {
        val tmp = Array(4) { UByteArray(4) }
        for (i in 0..3){
            for (j in 0..3){
                tmp[j][i] = state[j][i] xor roundKeys[(4*round)+i][j]
            }
        }
        return tmp
    }

    private fun subBytes(state: Array<UByteArray>): Array<UByteArray> {
        val tmp = Array<UByteArray>(4){UByteArray(4)}
        for (i in 0..3) {
            for (j in 0..3) {
                tmp[i][j] = generateSBox()[state[i][j].toInt()]
            }
        }
        return tmp
    }

    private fun invSubBytes(state: Array<UByteArray>): Array<UByteArray> {
        val tmp = Array<UByteArray>(4){UByteArray(4)}
        for (i in 0..3) {
            for (j in 0..3) {
                tmp[i][j] = generateInvSBox()[state[i][j].toInt()]
            }
        }
        return tmp
    }

    private fun shiftRows(state: Array<UByteArray>): Array<UByteArray> {
        val tmp = Array(4){UByteArray(4)}
        for (i in 0..3) {
            for (j in 0..3)
                tmp[i][j] = state[i][(i+j).mod(4)]
        }
        return tmp
    }

    private fun invShiftRows(state: Array<UByteArray>): Array<UByteArray> {
        val tmp = Array(4){UByteArray(4)}
        for (i in 0..3) {
            for (j in 0..3)
                tmp[i][j] = state[i][(j-i).mod(4)]
        }
        return tmp
    }

    private fun gMul(a: UByte, b: UByte): UByte {
        var a = a
        var b = b
        var p: UByte = 0u

        for (counter in 0..7) {
            if ((b.toInt() and 1) != 0) {
                p = (p.toInt() xor a.toInt()).toUByte()
            }

            val hiBitSet: Boolean = (a.toInt() and 0x80) != 0
            a = (a.toInt() shl 1).toUByte()
            if (hiBitSet) {
                a = (a.toInt() xor 0x1B).toUByte() /* x^8 + x^4 + x^3 + x + 1 */
            }
            b = (b.toInt() shr 1).toUByte()
        }

        return p
    }

    private fun mixColumns(state: Array<UByteArray>): Array<UByteArray> {
        val tmp = Array(4) { UByteArray(4) }
        for (i in 0..3) {
            tmp[0][i] = gMul(state[0][i], 2u) xor gMul(state[1][i], 3u) xor state[2][i] xor state[3][i]
            tmp[1][i] = state[0][i] xor gMul(state[1][i], 2u) xor gMul(state[2][i], 3u) xor state[3][i]
            tmp[2][i] = state[0][i] xor state[1][i] xor gMul(state[2][i], 2u) xor gMul(state[3][i], 3u)
            tmp[3][i] = gMul(state[0][i], 3u) xor state[1][i] xor state[2][i] xor gMul(state[3][i], 2u)
        }
        return tmp
    }

    private fun invMixColumns(state: Array<UByteArray>): Array<UByteArray> {
        val tmp = Array(4) { UByteArray(4) }
        for (i in 0..3) {
            tmp[0][i] = gMul(state[0][i], 14u) xor gMul(state[1][i], 11u) xor gMul(state[2][i], 13u) xor gMul(state[3][i], 9u)
            tmp[1][i] = gMul(state[0][i], 9u) xor gMul(state[1][i], 14u) xor gMul(state[2][i], 11u) xor gMul(state[3][i], 13u)
            tmp[2][i] = gMul(state[0][i], 13u) xor gMul(state[1][i], 9u) xor gMul(state[2][i], 14u) xor gMul(state[3][i], 11u)
            tmp[3][i] = gMul(state[0][i], 11u) xor gMul(state[1][i], 13u) xor gMul(state[2][i], 9u) xor gMul(state[3][i], 14u)
        }
        return tmp
    }

    override fun encryptData(data: UByteArray): UByteArray {
        if (data.size != 16)
            throw IllegalArgumentException("Block of data must be 16 byte long")

        var state = Array<UByteArray>(4){UByteArray(4)}
        for (i in 0..3) {
            for (j in 0..3) {
                state[i][j] = data[i+(4*j)]
            }
        }

        val roundKeys = key.genSubKeys()

        state = addRoundKey(state, roundKeys, 0)
        for (round in 1..<key.getNumberOfRounds()) {
            state = subBytes(state)
            state = shiftRows(state)
            state = mixColumns(state)
            state = addRoundKey(state, roundKeys, round)
        }
        state = subBytes(state)
        state = shiftRows(state)
        state = addRoundKey(state, roundKeys, key.getNumberOfRounds())

        val output = UByteArray(16)
        for (i in 0..3) {
            for (j in 0..3) {
                output[i+4*j] = state[i][j]
            }
        }

        return output
    }

    override fun decryptData(data: UByteArray): UByteArray {
        if (data.size != 16)
            throw IllegalArgumentException("Block of data must be 16 byte long")

        var state = Array<UByteArray>(4){UByteArray(4)}
        for (i in 0..3) {
            for (j in 0..3) {
                state[i][j] = data[i+(4*j)]
            }
        }

        val roundKeys = key.genSubKeys()

        state = addRoundKey(state, roundKeys, key.getNumberOfRounds())
        state = invShiftRows(state)
        state = invSubBytes(state)

        for (round in key.getNumberOfRounds()-1 downTo 1) {
            state = addRoundKey(state, roundKeys, round)
            state = invMixColumns(state)
            state = invShiftRows(state)
            state = invSubBytes(state)
        }
        state = addRoundKey(state, roundKeys, 0)


        val output = UByteArray(16)
        for (i in 0..3) {
            for (j in 0..3) {
                output[i+4*j] = state[i][j]
            }
        }

        return output
    }

